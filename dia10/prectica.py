mascotas = {   #creacion de un diccionario
    1: "perro",
    2: "gato",
}
print(mascotas)

#agregar un elemento
mascotas[3] = "loro"
print(mascotas)

#cambiar el valor a una clave
mascotas[2] = "hamster"
print(mascotas)

#eliminar clave:valor de un diccionario con del
del mascotas[1] 
print(mascotas)

